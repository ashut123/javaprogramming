public class SubString {
    public static void main(String[] args) {
        String str=new String("ashutosh");

        String str2= str.substring(3,7);   // it means start from 3 and upto ending-1 it will print
        // sub string will start print from given number and at ending-1 it will print the value
        System.out.println(str2);
    }
    
}
