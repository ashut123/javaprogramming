

public class ZZstringindexOf {
    public static void main(String[] args) {
        String str1=new String("www.ashu.co.in");
        System.out.println(str1.indexOf('.'));
        System.out.println(str1.indexOf("ashu"));
        System.out.println(str1.indexOf(".",4)); // it means after 4 where dot is appear.
        System.out.println(str1.lastIndexOf(".")); // it is used for to check last index number.

    }
    
}
