

public class Printing2 {
    public static void main(String[] args) {
        int x=10;
        float y=12.56f;
        char z='A';

        System.out.printf("Hello %d %f %c World\n",x,y,z);
        System.out.printf("Hello %o %f %c World\n",x,y,z); // here %o means it gives octal value

    }
       
    
}
