public class literal3 {
    public static void main(String arg[])
    {
         long l=999_999_999L; // in java on the place of comma we use underscore ans we can not use underscore in last
         //here we can not assign int value L because int is smaller and L is larger
         float f=12.56f;
         double d =12.56d;         
         

         System.out.println(l);
         System.out.println(f);
         System.out.println(d);
    }
    
}
